local In = {}
In.__index = In


-- ------------------------------------------------------------------------
-- constructors

function In.new(id)
  local p = setmetatable({}, In)
  p.id = id
  p.v = 0
  return p
end


-- ------------------------------------------------------------------------
-- accessors

function In:reset()
  self.v = 0
end


-- ------------------------------------------------------------------------

return In
